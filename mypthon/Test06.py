# operator ตัวดำเนินการ คืิอ เครื่องหมายที่ใช้ในการประมวลผลข้อมูล
# 1. Arithmetic Operator เครื่องหมายคำนวณ
print(10+3)
print(10-3)
print(10*3)
print(10/3)
print(10%3) #หารเอาเศษ
print(10//3) #หารเอาส่วน
print(10**3)
print('+++++++++++++++++++++++++++')
# 2. compare Operator เครื่องหมายเปรียบเทียบ
print(10==3)
print(10!=3)
print('Bangna'>='Bangpoo')
print('Thailand'<'THAILAND')
print('+++++++++++++++++++++++++++')
# 3. logical Operator เครื่องหมาย ตรรกะ
print(True and False) 
print(True or True) #True
print(False and False)
print(False and True)
print('---------------------------')
print(True or False)
print(True or True)
print(False or False) #False
print(False or True) 
print('---------------------------')
print(not True)
print(not False)
